public class AddQuestionImageAdapter extends BaseAdapter {

    private Context mContext;
    private List<String> allImagesList;

    public AddQuestionImageAdapter(Context c,List<String> allImagesList) {
        this.allImagesList=allImagesList;
        mContext = c;
    }

    public int getCount() {
        return allImagesList.size();
    }

    public Object getItem(int position) {
        return null;
    }

    public long getItemId(int position) {
        return 0;
    }

    // create a new ImageView for each item referenced by the Adapter
    public View getView(int position, View convertView, ViewGroup parent) {
        ImageView imageView;
        if (convertView == null) {
            // if it's not recycled, initialize some attributes
            imageView = new ImageView(mContext);
            imageView.setLayoutParams(new ViewGroup.LayoutParams(85, 85));
            imageView.setScaleType(ImageView.ScaleType.FIT_XY);
            imageView.setPadding(8, 8, 8, 8);
        } else {
            imageView = (ImageView) convertView;
        }

        //imageView.setImageResource(mThumbIds[position]);
        if(position<allImagesList.size())
            Glide.with(mContext).load(allImagesList.get(position)).into(imageView);
        return imageView;
    }
}

